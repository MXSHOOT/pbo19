class Kalkulator{
    public static void main (String args []) {
        System.out.println("Hasil dari 1+2 = " + (1+2) );
        System.out.println("Hasil dari 1+2 = " + (5-1) );
        System.out.println("Hasil dari 1+2 = " + (4/2) );
        System.out.println("Hasil dari 1+2 = " + (2*2) );
        System.out.println("Hasil dari 1+2 = " + (5%2) );
    }
}